namespace Kalkulacka
{
    public class Kalkulacka
    {
        public double Secti(double x, double y)
        {
            return x + y;
        }

        public double Odecti(double x, double y)
        {
            return x - y;
        }
        public double Vynasob(double x, double y)
        {
            return x * y;
        }

        public double Vydel(double x, double y)
        {
            return x / y;
        }

    }
}